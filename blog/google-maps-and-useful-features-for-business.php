						<p class="p-lg txt-justify">Your <a href="https://www.google.com/intl/en_in/business/" style="color:#0000FF;">Google Business Profile </a> (formerly known as Google My Business) will be used to populate your business's listing on Google Maps. If you want your company to stand out on Google Maps, you'll need to establish or claim your profile and make any necessary edits.</p>
						<!-- Text -->
                        <p class="p-lg txt-justify">To get started, create a Google Business Profile and fill it up as thoroughly as possible. They'll probably want to know things like:</p>
