                        <p class="p-lg txt-justify">During the Search On 2022 event, Google revealed a number of <a href="https://blog.google/products/maps/sustainable-immersive-maps-announcements/#:" style="color:#0000FF;">new features </a> and improvements coming to its Maps app. In the near future, there will be features such as an immersive view, neighborhood vibes, and search with live view. In addition, the colossal technology company has released more than 250 photorealistic aerial shots of famous places all around the world.</p>
                            
